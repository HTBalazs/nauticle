#include "pmFile.h"

/////////////////////////////////////////////////////////////////////////////////////////
/// Constructor.
/////////////////////////////////////////////////////////////////////////////////////////
pmFile::pmFile(std::string const& filename, std::string const& mode) {
	file = fopen(filename.c_str(), mode.c_str());
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Destructor.
/////////////////////////////////////////////////////////////////////////////////////////
pmFile::~pmFile() {
	fclose(file);
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Implements fprintf.
/////////////////////////////////////////////////////////////////////////////////////////
void pmFile::fprint(char const* fmt, ...) {
	va_list args;
	va_start(args, fmt);
	vfprintf(file, fmt, args);
	va_end(args);
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Implements vfprintf.
/////////////////////////////////////////////////////////////////////////////////////////
void pmFile::vfprint(char const* fmt, va_list args) {
	vfprintf(file,fmt,args);
}