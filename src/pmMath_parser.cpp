#include "pmMath_parser.h"

/////////////////////////////////////////////////////////////////////////////////////////
/// Removes white spaces.
/////////////////////////////////////////////////////////////////////////////////////////
void pmMath_parser::remove_spaces(std::string& str) const {
	std::string::iterator it = str.begin();
	while(it!=str.end()) {
		if(*it==' ') {
			str.erase(it, it+1);
		} else {
			it++;
		}
	}
}